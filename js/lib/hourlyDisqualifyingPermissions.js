/** Set for O(1) lookup */
const DISQUALIFYING_SET = new Set(HOURLY_DISQUALIFYING_PERMISSIONS);

/**
 * Check whether a single permission string is disqualifying.
 * @param {string} perm  e.g. "authorization:division:add"
 * @returns {boolean}
 */
export function isDisqualifying(perm) {
  return DISQUALIFYING_SET.has(perm);
}

/**
 * Given an array of permission strings, return those that are disqualifying.
 * @param {string[]} perms
 * @returns {string[]}
 */
export function filterDisqualifying(perms) {
  return perms.filter((p) => DISQUALIFYING_SET.has(p));
}

/**
 * Check whether ANY permission in the array is disqualifying.
 * @param {string[]} perms
 * @returns {boolean}
 */
export function hasAnyDisqualifying(perms) {
  return perms.some((p) => DISQUALIFYING_SET.has(p));
}
